function mfg(input: {
  slug: string
  name: string
  shortName?: string
  aliases?: string[]
  subtitle: string
  knownFor?: string
  popularPaths?: string
  peekSummary: string
  categories: ReturnType<typeof categoryRef>[]
  published?: boolean
  sortRank?: number
  manufacturerId?: string
}): ManufacturerDirectoryItem {
  const label = input.shortName ?? input.name.split(/\s+/)[0] ?? input.name

  return {
    slug: input.slug,
    name: input.name,
    shortName: input.shortName,
    aliases: input.aliases,
    subtitle: input.subtitle,
    knownFor: input.knownFor ?? input.subtitle.replace(/ · /g, ', '),
    popularPaths: input.popularPaths ?? `${label} alternatives and comparisons`,
    peekSummary: input.peekSummary,
    primaryCategoryL1: input.categories,
    letter: directoryLetterForName(input.name),
    href: `/manufacturers/${input.slug}`,
    published: input.published ?? false,
    sortRank: input.sortRank,
    manufacturerId: input.manufacturerId,
  }
}

